    .SYNOPSIS
    Produces a list of the top N (default 100) most common TCP ports according to
    the nmap services TSV file (/usr/share/nmap/nmap-services).

    .PARAMETER NumPorts
    The number of ports to retrieve. Default 100.

    .PARAMETER All
    Return all sorted 65536 ports. Default false.

    .EXAMPLE
    Get-TopPorts
    
    80
    23
    443
    21
    22
    25
    3389
    110
    445
    139
    ...

