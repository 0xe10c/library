function Test-TcpPort {
    <#
    .SYNOPSIS
    A fast LOLBAS PowerShell function for testing firewall egress ports.

    .DESCRIPTION
    Based on nmap SYN scan principle - if a SYN packet makes it through the 
    firewall to a WAN host, then that port is available for egress from the
    LAN side. If listening on a user-controlled WAN host, you can easily see
    which ports are available for egress. Can also be used as rudimentary
    port scanner from a LAN host to a non-controlled target.

    .PARAMETER ComputerName
    The IP address of the listening WAN host.

    .PARAMETER Port
    The port to test for egress. Can be passed in via pipeline.

    .PARAMETER TimeoutMs
    Timeout value in milliseconds. Default 500. Not strictly needed 
	if only testing egress ports. If using for recon, can be fine-tuned.

    .PARAMETER ShowResults
    Used to display "port scanning" results.  Default is false. Results
    are not reliable for egress testing - check listener on attacker.

    .EXAMPLE
    Test-TcpPort -ComputerName 10.10.14.44 -Port 80

    [*] Running in egress mode - check listener on attacker for egress ports

	.EXAMPLE
	80, 22, 8000, 8080, 8888, 9001 | Test-TcpPort -ComputerName 10.10.122.20 -ShowResults

	Host         Port  Open Reason
	----         ----  ---- ------
	10.10.122.20   80 False Timeout
	10.10.122.20   22  True Connected
	10.10.122.20 8000  True Connected
	10.10.122.20 8080 False Timeout
	10.10.122.20 8888 False Timeout
	10.10.122.20 9001  True Connected

    .EXAMPLE
    1..1024 | Test-TcpPort -ComputerName 10.10.14.44 -ShowResults

	Host        Port  Open Reason
	----        ----  ---- ------
	10.10.14.44    1 False Timeout
	10.10.14.44    2 False Timeout
	10.10.14.44    3 False Timeout
	10.10.14.44    4 False Timeout
	10.10.14.44    5 False Timeout
	10.10.14.44    6 False Timeout
	10.10.14.44    7 False Timeout
	10.10.14.44    8 False Timeout
	...

    #>
    param(
        [Parameter(Mandatory)]
        [string]$ComputerName,
        [Parameter(Mandatory, ValueFromPipeline)]
        [int]$Port,
        [int]$TimeoutMs = 100,
        [Switch]$ShowResults = $false
    )

    begin{
        if (-not $ShowResults) {
            Write-Host "[*] Running in egress mode - check listener on attacker for egress ports" -ForegroundColor Yellow
        }

    }
    process {
        $client = New-Object System.Net.Sockets.TcpClient
        try {
            $ar = $client.BeginConnect($ComputerName, $Port, $null, $null)
            $completed = $ar.AsyncWaitHandle.WaitOne($TimeoutMs)
            if (-not $completed) {
                # timeout -> assume blocked / drop
                $client.Close()
                if ($ShowResults) {
                    return [PSCustomObject]@{ Host = $ComputerName; Port = $Port; Open = $false; Reason = "Timeout" }
                }
            }

            try {
                $client.EndConnect($ar)
                $client.Close()
                if ($ShowResults) {
                    return [PSCustomObject]@{ Host = $ComputerName; Port = $Port; Open = $true; Reason = "Connected" }
                }
            } catch {
                $client.Close()
                if ($ShowResults) {
                    return [PSCustomObject]@{ Host = $ComputerName; Port = $Port; Open = $false; Reason = $_.Exception.Message }
                }
            }
        } finally {
            # ensure handle disposed
            if ($ar -and $ar.AsyncWaitHandle) { $ar.AsyncWaitHandle.Close() }
        }
    }
}

function Get-TopPorts {
